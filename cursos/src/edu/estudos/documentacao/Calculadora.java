public class Calculadora {
		/**
	 * Este métudo é ultilizado para somar dois numeros inteiros
	 * @param numeroUm este é o primeiro parâmetro do metudo
	 * @param numeroDois este é o segundo parâmetro do metudo
	 * @return int resultado deste metudo é a soma dos dois números.
	 */
	public int somar(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}
}
